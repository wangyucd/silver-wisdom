package com.silver.content.service.ai.prompt;

/**
 * AI Prompt 模板常量。
 * 集中管理所有 AI 场景的 Prompt 模板，便于维护和调整。
 *
 * @author wangyu03
 * @since 2026/04/30 10:00
 */
public final class AiPromptTemplates {

    private AiPromptTemplates() {
        // 工具类，禁止实例化
    }

    /**
     * 对话问答 Prompt 模板。
     * 参数：
     * - %s: 参考内容
     * - %s: 用户问题
     */
    public static final String CHAT_TEMPLATE = """
            你是一个专业的知识助手，专门服务于银发人群。
            请根据以下参考内容，回答用户的问题。

            参考内容：
            %s

            用户问题：
            %s

            要求：
            1. 回答要简洁明了，适合银发人群阅读
            2. 如果参考内容中有相关信息，请优先引用
            3. 如果参考内容中没有相关信息，请基于你的知识回答，但要说明这不是来自平台内容
            4. 回答长度控制在 200-400 字
            """;

    /**
     * 内容生成 Prompt 模板。
     * 参数：
     * - %s: 用户需求
     * - %s: 参考内容
     */
    public static final String GENERATE_TEMPLATE = """
            你是一个专业的知识内容生成助手，专门服务于银发人群。
            请根据用户的需求，生成一篇结构化的学习内容。

            用户需求：
            %s

            参考内容：
            %s

            要求：
            1. 内容要有深度和实用性
            2. 结构清晰，包含标题、摘要、大纲和正文
            3. 语言简洁易懂，适合银发人群阅读
            4. 内容长度适中（500-800字）
            5. 包含具体的建议和行动指南

            请按以下格式输出：
            【标题】xxx
            【摘要】xxx
            【大纲】xxx
            【正文】xxx
            """;

    /**
     * 标题生成 Prompt 模板。
     * 参数：
     * - %s: 关键词
     * - %s: 搜索结果
     */
    public static final String TITLE_GENERATE_TEMPLATE = """
            请根据以下搜索结果，为关键词 "%s" 生成一个吸引人的标题。

            搜索结果：
            %s

            要求：
            1. 标题长度在 10-20 字之间
            2. 简洁明了，能够吸引读者点击
            3. 突出内容的核心价值
            4. 不要使用过于夸张的词汇

            请只输出标题，不要添加任何前缀或解释。
            """;

    /**
     * 内容生成 Prompt 模板（基于搜索结果）。
     * 参数：
     * - %s: 关键词
     * - %s: 搜索结果
     */
    public static final String CONTENT_GENERATE_TEMPLATE = """
            请根据以下搜索结果，为关键词 "%s" 生成一篇高质量、有价值的内容。

            搜索结果：
            %s

            要求：
            1. 内容要有深度和专业性，不只是简单罗列信息
            2. 结构清晰，有适当的段落划分
            3. 包含实用价值和建议
            4. 语言简洁易懂，适合银发人群阅读
            5. 内容长度适中（500-800字）

            请直接输出生成的内容，不要添加"以下是生成的内容"等前缀。
            """;
}
